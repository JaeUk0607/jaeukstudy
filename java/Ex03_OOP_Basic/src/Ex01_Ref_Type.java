import kr.or.bit.*;

public class Ex01_Ref_Type {
	public static void main(String[] args) {
		// 설계도 기반으로 Person이라는 타입을 갖는 객체를 생성(heep)
		// new 연산자
		Person person; // 선언(static 4byte 공간 빈값(null))
		person = new Person(); // heep 메모리에 Person 객체 만들어지고 그 주소값을 person변수에 할당

		person.name = "홍길동";
		person.age = 100;
		person.print();

		Person my = new Person(); // 선언하고 메모리 할당을 동시에....
		my.name = "마이마이";
		my.age = 10;
		my.print();

		int num = 100; // main [함수]안에 선언된 변수 local variable (반드시 초기화)

		Person you; // 변수
		// System.out.println(you); //The local variable you may not have been
		// initialized
		// 참조타입 초기화
		// 1. 초기화 (new 연산자)
		// you = new Person();//참조타입 초기화(변수가 처음 값을 같는 것)

		// 2. 참조 변수가 가지는 주소값 할당
		you = my; // 주소값 할당(초기화) 처음 값을 같는 행위
		System.out.println(you); // kr.or.bit.Person@70dea4e

		// you 참조변수와 my 참조변수의 주소값이 같음을 증명해서
		System.out.println(you == my); // 값비교 (같은 주소 : true)
		System.out.println(you + " = " + my);

		Tv t = new Tv();
		t.name = "재욱짱";
		
		System.out.println("기본 채널 : "+ t.ch);
		System.out.println("브랜드 이름 :" + t.name); //null 값이 없다
		
		t.ch_up();
		t.ch_up();
		t.ch_up();
		System.out.println("변경된 채널 : " + t.ch);
		
		t.ch_down();
		System.out.println("변경된 채널 : " + t.ch);
		
	}

}
